    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       String fechaStr = jTextField1.getText();
       DateTimeFormatter formatter1 = DateTimeFormatter.ofPattern("dd/MM/yyyy"); 
       LocalDate fechaLocalDate = LocalDate.now();// Formato deseado
       try{
           fechaLocalDate = LocalDate.parse(fechaStr,formatter1);
       }catch(DateTimeParseException e){
           System.out.println("La cadena no es valida" + e.getMessage());
       }
       DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
       try{
        ArrayList<Usuario> usuarios = userService.recuperaUsuariosPorFecha(fechaLocalDate);
        model.setRowCount(0); // Limpiar la tabla

        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy"); // Formato deseado

        for (Usuario usuario : usuarios) {
            //String fechaFormateada = usuario.getUltimoAcceso().format(formatter);
            Object[] row = {usuario.getNombre(),usuario.getApellidoPat(),usuario.getApellidoMat(),usuario.getUltimoAcceso()};
            model.addRow(row);
        }
    } catch (Exception e) {
        model.setRowCount(0);// Manejar errores (por ejemplo, mostrar un mensaje al usuario)
        javax.swing.JOptionPane.showMessageDialog(this, "No hay usuarios activos desde esa fecha" );
        
    }
    }//GEN-LAST:event_jButton2ActionPerformed

