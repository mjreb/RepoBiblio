    //En este metodo se busca el libro correspondiente al id ingresado
    private void jButtonBuscarIDLibroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarIDLibroActionPerformed
        //Este try-catch captura los siguientes casos:
        //1-El usuario ingresa un id invalido (por ejemplo letras o deja el espacio en blanco
        //2-El id ingresado no corresponde a un libro en la base de datos
        try{
            int idLibro=Integer.parseInt(jTextFieldIDLibro.getText());
            JOptionPane.showMessageDialog(this, itemInventarioService.recuperaInventarioPorIdLibro(idLibro));
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "ID invalido","Error",JOptionPane.ERROR_MESSAGE);
        }catch(RuntimeException e){
            JOptionPane.showMessageDialog(this, "Libro no encontrado","Error",JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButtonBuscarIDLibroActionPerformed

