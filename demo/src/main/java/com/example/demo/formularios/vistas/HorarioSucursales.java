//En este metodo se muestran las sucursales
    private void jButtonBuscarSucursalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarSucursalActionPerformed
        String opcion=jComboBoxSucursales.getSelectedItem().toString();
        //De acuerdo a lo seleccionado en el combobox se mostrara un mensaje u otro, con la 
        //direccion de la sucursal elegida
        if(opcion.equals("Zocalo")){
            JOptionPane.showMessageDialog(this,"Direccion: San Salvador #12 Colonia Centro\n"
                    + "Horario: Lunes a viernes: 8:00 a 19:00");
        }else if(opcion.equals("Santa Fe")){
            JOptionPane.showMessageDialog(this,"Direccion: Tlalpan 14\n"
                    + "Horario: Lunes a viernes: 8:00 a 19:00");
        }
    }//GEN-LAST:event_jButtonBuscarSucursalActionPerformed

