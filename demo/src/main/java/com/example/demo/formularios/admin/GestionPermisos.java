    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        int filaSeleccionada = jTable1.getSelectedRow();
        if(filaSeleccionada >=0){
            Long idUsuario = (long)jTable1.getValueAt(filaSeleccionada,0);
            boolean permisoActivo = Prestamo.isSelected();
            
            userService.actualizarPermiso(idUsuario, permisoActivo);
            cargarUsuarios();
            javax.swing.JOptionPane.showMessageDialog(this, "Permiso guardado");
            
        }else{
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, seleccione un usuario");
        }
    }//GEN-LAST:event_GuardarActionPerformed

