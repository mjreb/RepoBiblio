    @PostConstruct
    private void cargarUsuarios(){
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido Paterno");
        modelo.addColumn("Correo");
        
        //List<Usuario> usuarios = userService.recuperaUsuarios();
        usuarios = userService.recuperaUsuarios();
        for (Usuario usuario :usuarios){
            modelo.addRow(new Object[]{usuario.getidUsuario(), usuario.getNombre(),usuario.getApellidoPat(), usuario.getCorreo()});
        }
        jTable1.setModel(modelo);
        
        usuarios = userService.recuperaUsuarios();
        filtrarUsuarios("");
        agregarListeners();
        
        Buscar.addKeyListener(new java.awt.event.KeyAdapter() {
            public void keyReleased(java.awt.event.KeyEvent evt){
                filtrarUsuarios(Buscar.getText());
            }   
        });
    }
    
    private void agregarListeners(){
        jTable1.getSelectionModel().addListSelectionListener(e ->{
            if (!e.getValueIsAdjusting()){
                int filaSeleccionada = jTable1.getSelectedRow();
                if (filaSeleccionada >=0 && filaSeleccionada < usuarios.size()){
                    Usuario usuarioSeleccionado = usuarios.get(filaSeleccionada);
                    Prestamo.setSelected(usuarioSeleccionado.getPermisosPrestamo());
                }
            }
        });
    }
    
    private void filtrarUsuarios(String texto){
        DefaultTableModel modelo= (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);
        
        texto = texto.toLowerCase();
        
        for(Usuario usuario: usuarios){
            String id = String.valueOf(usuario.getidUsuario());
            String nombre = usuario.getNombre().toLowerCase();
            String apellido = usuario.getApellidoPat().toLowerCase();
            String correo = usuario.getCorreo().toLowerCase();
            
            if(id.contains(texto) || nombre.contains(texto) || apellido.contains(texto) || correo.contains(texto)){
                modelo.addRow(new Object[]{
                    usuario.getidUsuario(),
                    usuario.getNombre(),
                    usuario.getApellidoPat(),
                    usuario.getCorreo()
                });
            }
            
        }
    }
